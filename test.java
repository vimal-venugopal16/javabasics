public class HelloWorld {

    public static void main(String[] args) {
        // Print a message to the console
        System.out.println("Hello, this great World!");

        // Basic variables
        int number = 42;
        String name = "Alice";

        // Conditional statement
        if (number > 10) {
            System.out.println(name + " has a large number.");
        } else {
            System.out.println(name + " has a small number.");
        }

        // Loop example
        for (int i = 1; i <= 5; i++) {
            System.out.println("Counting: " + i);
        }
    }
}
